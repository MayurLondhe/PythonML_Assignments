# What is diffrence between:

# - Funtion with parameters
# Ans - Function with parameters is function that 
# accept parameters to execute.
# Example :

n1 = 10
n2 = 20

def addition(n1,n2):
    sum = n1+n2
    return sum

print("Function with parameters :",addition(n1,n2))

# - Funtion without parameters
# Ans - Function without parameters is function that 
# not accept any parameters to execute.
# Example :

def addition1():
    sum = n1+n2
    return sum

print("Function without parameters :",addition1())

